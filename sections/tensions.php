<div class="sec" id="tensions">Tensions & contradictions centrales</div>
  
  <div class="g2">
    <div class="warn">
      <i class="ti ti-alert-triangle" class="icon" aria-hidden="true"></i>
      <strong>Hypocrisie classe sociale</strong><br>
      Se dit méprisant envers les classes populaires et immigrés, mais s'approvisionne discrètement en drogue via des contacts de banlieue. Discours et comportements en contradiction totale.
    </div>
    <div class="warn">
      <strong>Policier &amp; criminel simultanément</strong><br>
      Stagiaire officiel à la police, Jeremy gère en parallèle une fraude associative, une consommation de drogue, un tournage de propagande politique, et des manipulations actives. Une seule découverte suffit à tout faire s'effondrer — d'autant que le père de sa complice Alix est un ancien haut gradé.
    </div>
    <div class="info-box">
      <i class="ti ti-brain" class="icon" aria-hidden="true"></i>
      <strong>Frustration profonde</strong><br>
      Le handicap et la privation financière alimentent une rage contenue. L'arrogance et la manipulation sont des mécanismes de compensation face à un sentiment d'impuissance réel.
    </div>
    <div class="info-box">
      <strong>Relation Alix — bombe à retardement</strong><br>
      La psychologue a conseillé à Jeremy de couper les ponts. Si Alix est vraiment opportuniste, la fausse association pourrait se retourner contre lui. Et si son père Jean Lefèvre — ancien haut gradé de police — venait à s'en mêler, les conséquences seraient disproportionnées.
    </div>
    <div class="warn">
      <strong>L'incident Leblanc — vengeance accomplie, guerre ouverte — 25/05→31/05</strong><br>
      L'incident du Céleste a déclenché une guerre : Leblanc a tendu un piège à Jeremy le 31/05, l'humiliant et le photographiant dans une mise en scène dégradante. La vengeance est faite — mais Jeremy n'est pas du genre à encaisser sans réagir. Ce cycle de représailles risque de s'emballer.</div>
    <div class="warn">
      <strong>Vautrin & le tournage — soldé mais risqué — 28/05→31/05</strong><br>
      Les 10 000 € sont récupérés via Valmont. Financièrement clos — mais Jeremy reste exposé : il a participé à un film de propagande politique et l'alliance avec Louis de Villiers a impliqué des témoins. Un dossier qui pourrait resurgir.
    </div>
    <div class="warn">
      <strong>Potentielle plainte Palais de Jade — 28/05</strong><br>
      Jeremy a refusé de payer 500 € de cours de kung-fu. Les responsables du Palais de Jade envisagent une plainte pour escroquerie. Nouvelle épée de Damoclès pour Jeremy, qui accumule les fronts ouverts.
    </div>
    <div class="warn">
      <strong>Rita suspendue — 29/05</strong><br>
      En cherchant à aider, Jeremy a fait intervenir Rita hors procédure au commissariat. Résultat : sa médecin de confiance est temporairement suspendue et lui en tient rigueur. Un soutien précieux s'effrite au moment où Jeremy enchaîne les incidents physiques.
    </div>
    <div class="warn">
      <strong>Enquêtes policières & double vie — 29/05</strong><br>
      Jeremy est impliqué dans des interrogatoires sur des affaires sérieuses (Botola, Montclair) tout en ayant lui-même des connexions à Évry via Geremy. Sabri Kelma, désormais suspect actif, est quelqu'un que Jeremy connaît indirectement. La famille Nakache, nouvellement évoquée, est un nom à ne pas croiser dans les deux mondes simultanément.
    </div>
    <div class="warn">
      <strong>Taupe dans la police — 31/05</strong><br>
      Valmont a glissé à Jeremy qu'une taupe pourrait se trouver au sein de la police parisienne. Information sensible pour quelqu'un qui mène une double vie entre milieu criminel et commissariat — et qui pourrait lui-même être observé.
    </div>
    <div class="warn">
      <strong>Mystère Larouge — 31/05</strong><br>
      Des documents suggèrent que les enfants Larouge auraient plus de cent ans malgré leur apparence de jeunes adultes. Ce mystère rejoint directement l'affaire du majordome Denolo et la disparition de Philomène Larouge — une piste surnaturelle ou une manipulation à grande échelle ?
    </div>
    <div class="warn">
      <strong>Ambrose — surprise secrète — 31/05</strong><br>
      Ambrose de Saint-Claire prépare quelque chose dont il attend la livraison, en demandant expressément à Alix de ne rien dire. Pour un personnage jusqu'alors effacé, ce comportement attire tous les soupçons.
    </div>
