<div class="sec" id="tensions">Tensions & contradictions centrales</div>
  
  <div class="g2">
    <div class="warn">
      <i class="ti ti-alert-triangle" class="icon" aria-hidden="true"></i>
      <strong>Hypocrisie classe sociale</strong><br>
      Se dit méprisant envers les classes populaires et immigrés, mais s'approvisionne discrètement en drogue via des contacts de banlieue. Discours et comportements en contradiction totale.
    </div>
    <div class="warn">
      <strong>Policier &amp; criminel simultanément</strong><br>
      Stagiaire officiel à la police, Jeremy gère en parallèle une fraude associative, une consommation de drogue, un tournage de propagande politique, et des manipulations actives. Une seule découverte suffit à tout faire s'effondrer — d'autant que le père de sa complice Alix est un ancien haut gradé.
    </div>
    <div class="info-box">
      <i class="ti ti-brain" class="icon" aria-hidden="true"></i>
      <strong>Frustration profonde</strong><br>
      Le handicap et la privation financière alimentent une rage contenue. L'arrogance et la manipulation sont des mécanismes de compensation face à un sentiment d'impuissance réel.
    </div>
    <div class="info-box">
      <strong>Relation Alix — bombe à retardement</strong><br>
      La psychologue a conseillé à Jeremy de couper les ponts. Si Alix est vraiment opportuniste, la fausse association pourrait se retourner contre lui. Et si son père Jean Lefèvre — ancien haut gradé de police — venait à s'en mêler, les conséquences seraient disproportionnées.
    </div>
    <div class="warn">
      <strong>L'incident Leblanc — vengeance accomplie, guerre ouverte — 25/05→31/05</strong><br>
      L'incident du Céleste a déclenché une guerre : Leblanc a tendu un piège à Jeremy le 31/05, l'humiliant et le photographiant dans une mise en scène dégradante. La vengeance est faite — mais Jeremy n'est pas du genre à encaisser sans réagir. Ce cycle de représailles risque de s'emballer.</div>
    <div class="warn">
      <strong>Vautrin & le tournage — soldé mais risqué — 28/05→31/05</strong><br>
      Les 10 000 € sont récupérés via Valmont. Financièrement clos — mais Jeremy reste exposé : il a participé à un film de propagande politique et l'alliance avec Louis de Villiers a impliqué des témoins. Un dossier qui pourrait resurgir.
    </div>
    <div class="warn">
      <strong>Potentielle plainte Palais de Jade — 28/05</strong><br>
      Jeremy a refusé de payer 500 € de cours de kung-fu. Les responsables du Palais de Jade envisagent une plainte pour escroquerie. Nouvelle épée de Damoclès pour Jeremy, qui accumule les fronts ouverts.
    </div>
    <div class="warn">
      <strong>Rita suspendue — 29/05</strong><br>
      En cherchant à aider, Jeremy a fait intervenir Rita hors procédure au commissariat. Résultat : sa médecin de confiance est temporairement suspendue et lui en tient rigueur. Un soutien précieux s'effrite au moment où Jeremy enchaîne les incidents physiques.
    </div>
    <div class="warn">
      <strong>Enquêtes policières & double vie — 29/05</strong><br>
      Jeremy est impliqué dans des interrogatoires sur des affaires sérieuses (Botola, Montclair) tout en ayant lui-même des connexions à Évry via Geremy. Sabri Kelma, désormais suspect actif, est quelqu'un que Jeremy connaît indirectement. La famille Nakache, nouvellement évoquée, est un nom à ne pas croiser dans les deux mondes simultanément.
    </div>
    <div class="warn">
      <strong>Taupe dans la police — 31/05</strong><br>
      Valmont a glissé à Jeremy qu'une taupe pourrait se trouver au sein de la police parisienne. Information sensible pour quelqu'un qui mène une double vie entre milieu criminel et commissariat — et qui pourrait lui-même être observé.
    </div>
    <div class="warn">
      <strong>Mystère Larouge — confirmé & mission active — 31/05→04/06</strong><br>
      Jean Lefèvre confirme lui-même que les Larouge ne vieillissent pas. Jeremy et Alix sont désormais mandatés pour les approcher discrètement via un "tea time" à venir. La policière Pichon, proche du fils Larouge, ajoute une dimension policière au mystère.
    </div>
    <div class="warn">
      <strong>Ambrose — surprise secrète — toujours en suspens — 31/05→04/06</strong><br>
      La livraison mystérieuse d'Ambrose n'a toujours pas eu lieu. Alix continue de s'interroger. Pour un personnage jusqu'alors effacé, ce silence prolongé renforce les soupçons.
    </div>
    <div class="warn">
      <strong>Corruption policière généralisée — 04/06</strong><br>
      Plus une taupe isolée mais plusieurs policiers impliqués selon Vautrin — et Jean Lefèvre confirme sa méfiance envers le commissaire. Jeremy, stagiaire de police avec une double vie criminelle, navigue dans une institution minée de l'intérieur.
    </div>
    <div class="warn">
      <strong>Pichon — nœud de connexions dangereuses — 04/06</strong><br>
      Pichon est à la fois proche du fils Larouge (mystère de la famille) et sœur de César (complice de la fraude associative d'Alix et Jeremy). Si elle enquête ou parle, elle peut faire tomber plusieurs dossiers simultanément.
    </div>
    <div class="warn">
      <strong>Mort du maire & chaos politique — 04/06</strong><br>
      La mort du maire de Paris rebat toutes les cartes : élections municipales, équilibres de pouvoir, enquêtes en cours. Valmont, Mouflard, Vautrin — tous les personnages politiques que Jeremy fréquente sont directement affectés.
    </div>
    <div class="warn">
      <strong>Un certain Claude — commanditaire ? — 04/06</strong><br>
      Vautrin mentionne un individu nommé Claude, potentiellement impliqué dans le meurtre de son garde du corps. Identité et motivations inconnues — mais le nom circule dans des sphères dangereuses.
    </div>
    <div class="warn">
      <strong>Groupe militant & menace sur la police — 05/06</strong><br>
      Un groupe armé se réclamant de la gauche militante a enlevé Blanche Pichon et menacé de "faire tomber un policier chaque jour", ciblant directement le commissaire. L'un des membres répond au nom de <strong>Claude</strong> — première apparition directe. Jeremy est désormais témoin et cible potentielle en tant que policier.
    </div>
    <div class="warn">
      <strong>Leblanc — commanditaire de meurtre — 05/06→07/06</strong><br>
      La rivalité avec Leblanc dépasse désormais le cadre personnel : selon l'informateur Blanchard, Leblanc serait le commanditaire du meurtre attribué à Claude Garnier (FTG-FTC). Jeremy détient une information explosive — mais sans preuve formelle et sans pouvoir la transmettre officiellement sans exposer ses propres activités illicites.
    </div>
    <div class="warn">
      <strong>500 000 € volés à Valmont · incendie du Paradise — 07/06</strong><br>
      Alix a volé 500 000 € de subventions à Valmont. Jeremy est complice par omission. L'incendie du Paradise a probablement effacé les traces — mais Valmont va chercher son argent. Quand il découvrira le vol, les conséquences seront imprévisibles pour Alix et Jeremy.
    </div>
    <div class="warn">
      <strong>Mystère Larouge — poupées & Capétiens — 07/06</strong><br>
      Les révélations de Danie font basculer l'affaire Larouge dans le surnaturel : salle secrète, poupées de cire géantes, immortalité photographiée, vrai nom Capet. Nancy Larouge "transformerait" des personnes qui attirent son attention. Jeremy et Alix ont prévu d'infiltrer la propriété — une opération à très haut risque.
    </div>
